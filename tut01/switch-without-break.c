#include <stdio.h>

int main() {
    char ch = 'i';
    switch (ch) {
        case 'a':
            printf("eh? ");
        case 'e':
            printf("eee ");
        case 'i':
            printf("eye ");
        case 'o':
            printf("ohh ");
        case 'u':
            printf("you ");
    }
    printf("\n");
}
